
public class 산술연산자_3 {

	public static void main(String[] args) {

		int a = 5;
		int b = 2;
		System.out.printf("%d %% %d = %d \n",  a, b, a%b);
		System.out.printf("%d %% %d = %d \n",  -a, b, -a%b);
		System.out.printf("%d %% %d = %d \n",  a, -b, a%-b);
		System.out.printf("%d %% %d = %d \n",  -a, -b, -a%-b);

		// 입력을 받아서 윤년여부 확인  ==> 알고리즘 공식 (예매)
	}

}
